     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = $request->user();
        $payload =  $request->only(["post_id", "comment"]);
        $payload["user_id"] = $user->id;
        Comment::create($payload);
        $comment = view('partials.comment', ["name" => $user->name, "comment" => $payload["comment"]])->render();
        return response()->json(["status" => 200, "message" => "Comment added successfully", "comment" => $comment]);
    }

